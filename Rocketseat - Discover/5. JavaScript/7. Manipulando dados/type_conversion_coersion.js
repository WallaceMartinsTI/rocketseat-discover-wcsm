/*
    Type conversion (typecasting) vs Type coersion

    * Alteração de um tipo de dado para outro tipo
*/

console.log('9' + 5)    // Transforma o 5 em string implicitamente e concatena, retornando 95
console.log(Number('9') + 5)    // Transforma explicitamente o '9' em número e soma, retornando 14